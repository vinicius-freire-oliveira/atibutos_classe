# Define a classe Pessoa sem nenhum atributo ou método
class Pessoa:
    pass  # 'pass' é uma instrução que indica que nada acontece, usada como um placeholder

# Cria uma instância da classe Pessoa
pessoa = Pessoa()

# Adiciona um atributo 'nome' ao objeto 'pessoa' e define seu valor como 'Debby Harry'
pessoa.nome = 'Debby Harry'

# Imprime o valor do atributo 'nome' do objeto 'pessoa'
print(pessoa.nome)  # Saída: Debby Harry
